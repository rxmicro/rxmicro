package io.rxmicro.config;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * Indicates that annotated config class must be singleton.
 *
 * <p>
 * It means that only default namespace can be used with the annotated config class!
 *
 * @author nedis
 * @see Configs#getConfig(String, Class)
 * @since 0.8
 */
@Documented
@Retention(RUNTIME)
@Target(TYPE)
public @interface SingletonConfigClass {
}
